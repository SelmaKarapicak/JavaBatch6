package com.syntax.class07;

public class Loopspractice {

	public static void main(String[] args) {
		// how to print 50 to 1
		
		int num=50;
		while(num>=1) {
			System.out.println(num);
			num--;
		}
		
		System.out.println("*******");
		
		//how to print odd numbers from 1 to 20
		
		int x=0;
		while(x<=9) {
			System.out.println((x*2)+1);
			x++;
		}
		

	}

}
