package com.syntax.class07;

public class ForLoopsTask {

	public static void main(String[] args) {
		// Print numbers from 1 to 100 in 1 line with space
		
		for(int x=1; x<100; x++) {
			System.out.print(x+" ");
		}
		System.out.println("****");
		
	//Print numbers from 100 to 1
		
		for(int y=100; y>1; y--) {
			System.out.println(y);
		}
		
		
		

	}

}
