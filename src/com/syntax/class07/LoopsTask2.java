	public static void main(String[] args) {
		//Print even numbers from 20 to 1 (2 ways)
        //Print odd numbers between 20 and 50 (2 ways)
		
	
		int num=10;
		while(num>=1) {
			System.out.println(num*2);
			num--;
		}
		System.out.println("********");
		
		int num2=9;
		while(num2<=50) {
			System.out.println((num2*2)+1);
			num2++;
		}
		
			
		
		
		
		

	}

