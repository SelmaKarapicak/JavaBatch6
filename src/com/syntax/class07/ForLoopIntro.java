package com.syntax.class07;

public class ForLoopIntro {

	public static void main(String[] args) {
		// Good Morning 5 times
		
		for(int c=1; c<6; c++) {
			System.out.println("Good Morning");
		}
		
		//what is the output
		int sum=0;
		for(int i=1; i<5; i++) {
			sum=sum+i;
		}
		
		System.out.println(sum);
		
		
	}

}
