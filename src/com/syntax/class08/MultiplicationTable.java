package com.syntax.class08;

public class MultiplicationTable {

	public static void main(String[] args) {
		/* create multiplication table
		 * 1x1=1
		 * 1x2=2
		 * 1x3=3
		 */
       
		int num=4;
		
		for(int i=1; i<=10; i++) {
			System.out.println(num+" *"+i+"= "+i*num);
		}
		
		
		
		
		
		
	}

}
